          <div class="field field-name-body field-type-text-with-summary field-label-hidden"><div class="field-items"><div class="field-item even" property="content:encoded"><p>Donating blood, plasma or platelets in Australia is very safe. You can make it even safer and easier by making sure you’re healthy and have plenty to eat and drink before and after.</p>
<p><strong>You may not be able to donate, if on the day you have:</strong></p>
<ul><li>A cold or flu (no matter how mild)</li>
<li>Had an upset stomach in the past week</li>
<li>Had the COVID-19 vaccine in the past week. </li>
</ul><p>There are some other reasons you may not be able to donate on the day (to protect your health and patients that receive blood). We recommend taking our quick  before you come in.</p>
<p>Our donor centres are safe places to visit and we’re taking all necessary steps to ensure that stays the case.</p>
<p>If you have any questions, give us a call on13 14 95 and we'll gladly help and book you in for another time if needed.</p>
