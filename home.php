      <div class="col-6">
        <h2>Before you donate</h2>
        <h3>The day before</h3>
        <ul><li>Drink up! Your blood volume goes down a little when you donate. Drink plenty of fluids the day before, especially in warm weather. We recommend at least 10 glasses if you’re a man or 8 if you’re a woman. If you haven’t had enough water the day before, you should be fine to donate as long as you’ve had the same amount of fluids you’d normally drink before donating. If you’re a new donor or haven’t had enough, it’s best to reschedule.</li>
        <li>Write down or remember any medications you’re currently taking</li>
        <li>Get your ID together. <i>(e.g. Driver’s licence, Donor Card [including your Digital Donor Card in the Donate Blood app], passport)</i>. You’ll need to bring one or more documents to your appointment that, when combined, show at least three of these:
        <ul><li>Full name</li>
        <li>Date of birth</li>
        <li>Home address</li>
        <li>Signature</li>
        <li>Photo (if possible)</li>
        </ul></li>
        <li>Try to get a good night's sleep</li>
        </ul><h3>In the 3 hours before</h3>
        <ul><li>Drink 750mL (3 good-sized glasses) of fluids</li>
        <li>Have something savoury to eat</li>
        <li>Avoid exercise or strenuous activities</li>
      </div>
      <div class="col-6">
      </ul><h2>After you donate</h2>
      <ul><li>Stay in our refreshments area for 15-20 minutes and enjoy a cool drink and savoury snack</li>
      </ul><h3>In the next 8 hours</h3>
      <ul><li>Drink plenty of fluids - at least 3 good-sized glasses of water in the first 3 hours</li>
      <li>Don’t stand for long periods. Avoid long queues or standing on crowded public transport</li>
      <li>Try not to overheat. Avoid hot showers, rushing around, sitting or standing in direct sun, and hot drinks</li>
      <li>Avoid alcohol</li>
      <li>Eat regular meals</li>
      </ul><h3>For at least 12 hours</h3>
      <p>Avoid strenuous exercise (like cycling, jogging or going to the gym) or hazardous activities, including activities or jobs where public safety may be affected. You should check any employment or safety requirements you have. If unsure, please discuss at your interview.</p>
      <p>If you feel unwell after you donate, or have any questions, just call us on 13 14 95.</p>
